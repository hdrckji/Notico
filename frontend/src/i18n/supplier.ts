export const getStoredLang = (): Lang => {
  const stored = localStorage.getItem('supplierLang');
  if (stored === 'fr' || stored === 'nl' || stored === 'en') return stored;
  return 'fr';
};

export const setStoredLang = (lang: Lang) => {
  localStorage.setItem('supplierLang', lang);
};
